package fr.uge.poo.paint.ex6;

public class WindowsSize {

    private final int width;
    private final int height;

    public WindowsSize(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
